             */
            jQuery(".layer-element").each(function(index, el) {

                var doPosition = function() {
                    var _el = jQuery(el),
                        dataStartX = parseInt((typeof _el.attr('data-start-x') !== typeof undefined &&
                            _el.attr('data-start-x') !== false) ? _el.attr('data-start-x') : '50%') /
